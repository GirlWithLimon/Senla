package bookstore_app.config;

public enum PropertyType {
    AUTO,        // Автоматическое определение типа
    STRING,      // Строковый тип
    INTEGER,     // Целочисленный тип
    DOUBLE,      // Число с плавающей точкой
    BOOLEAN,     // Логический тип
    ARRAY,       // Массив строк
    LIST         // Список строк
}